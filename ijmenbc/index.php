<p><i>The Indian Journal of Medical Ethics (IJME)</i> (formerly <i>Medical Ethics and Issues in Medical Ethics</i>), a peer-reviewed, indexed journal, published since 1993, has been a forum for discussion on healthcare ethics for the last two decades. With the evolution of bioethics in India, the need emerged to share ideas, promote original indigenous thinking and research, and develop an ethical framework suitable for the Indian context.  The <i>IJME</i> National Bioethics Conferences (NBC) took shape against this background, as a platform for scientific exchanges and debate between different stakeholders in the field.  The first NBC was held in Mumbai in 2005, and the second and third NBCs in Bengaluru and Delhi in 2007 and 2010 respectively.</p>

<p>The 4th NBC is to be held in the historic city of Hyderabad, well known for institutes that excel in education and research. </p>

<p>The 4th NBC is intended to bring together the different key constituencies and individuals to share their diverse perspectives on the conference theme <i>�Ethical and regulatory challenges in health research�</i> with the aim of building a better research enterprise and a favourable environment for of ethical health research. The three-day conference will include five plenary sessions. While the inaugural and closing ceremonies will focus on the broad theme of the conference, the remaining three plenary sessions will focus on specific aspects of health research ethics: (1) interdisciplinary approaches to ethics, (2) clinical trials, and (3) research regulation laws and ethics.<br> 
Parallel sessions will be organised with oral presentations, interactive workshops, symposia and poster sessions. </p>

