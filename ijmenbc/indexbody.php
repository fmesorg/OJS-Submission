 <b> <a href="http://www.ijme.in/161nbc29.html" class="lnk">Bioethics and ayurveda</a> </b>
</font> <br />
<font face="arial" size="2">
	Bioethics, which is the title of this national conference, is a term that implies far more 
	than medical ethics which dominates its sessions. What is bioethics? It is no more and no 
	less than the ethics of living or ethics of life, which evolved from non-life over millions 
	of years. Humans are a product of the evolutionary process like all other species, but they 
	are unique insofar as they not only participate in the evolutionary process but also command 
	and determine the future, as brought home so vividly by global warning. Nevertheless, 
	as Professor Hubert Markl remarked, there is a circular relationship between nature and humankind
	 because human concepts are nature's concepts. Human technological and economic inventiveness is 
	 no more than nature's way of acting upon itself and shaping its own future. The outcome could be
	 glorious success or disastrous failure - in either case, nature acting through humankind bears partial
	 responsibility for the outcome. This is fundamentally an ethical question. Are we right, for example, 
	 to cause the profoundest changes in biodiversity in all the 3 billion years of evolution by the mindless
	 destruction of all living species to accommodate 10 billion human beings and their domesticated slave 
	 species of animals and plants? To make a sustainable future, we have an obligation to act in accordance
	 with the dictates of reason and moral norms, and remain responsible for what we do. It is this undoubted
	 fact of nature that makes us look for guidance to bioethics, without which life would be replaced by fossils.
	 I would, therefore, compliment the organisers for placing the deliberations of this conference against the 
	 austere background of bioethics.
