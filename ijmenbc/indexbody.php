<font face="arial" size="2">
	It was towards the end of the last century that medical ethics assumed the character of a distinct discipline
	in India, attracting the attention of professionals from both law and medicine. However, bioethics was not 
	a major issue in professional discourse at that time. The focus was more on technological applications in 
	medicine and healthcare, and their human rights implications. The concern was on medical negligence and the
	need for consumer protection.
