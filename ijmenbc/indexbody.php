<font face="arial" size="2">
	The recently concluded Indian Journal of Medical Ethics Second National Bioethics Conference brought together
	over 500 delegates, speakers and other participants from a wide range of disciplines. The participants were 
	a blend of healthcare professionals, academicians, researchers, media professionals and, interestingly, many
	retired professionals and laypersons. Though doctors and researchers represented the majority, the blend 
	clearly reflected the growing interest in bioethics amongst people of all walks of life and an increase in 
	the strength and momentum of the bioethics movement in India.
