<font face="arial" size="2"><b><a href="http://www.ijme.in/141cr27.html" class="lnk"> Reporting on the First National Bioethics Conference</a></b> </font><br />
<font face="arial" size="2">
	The First National Bioethics Conference, November 25-27, 2005 (NBC, 2005), organised by the Indian Journal
	of Medical Ethics (IJME), brought together a diverse group of organisations and individuals in an effort
	to give a platform for bioethics discourse from across the country. The conference was organised around
	four issues: ethical challenges in HIV/AIDS; ethics of life and death in the era of high-tech health care;
	ethical responsibilities in violence, conflict and religious strife; and ethics and equity in clinical trials.
	Three cross-cutting themes that emerged during discussions were ethical challenges in biomedical and social 
	science research, ethical responsibilities of clinical care providers, and the intersections of bioethics 
	and public health/clinical medicine.
