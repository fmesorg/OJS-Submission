The preconference workshop on publication ethics addressed some of the ethical issues related to publishing of scientific research. Participants at the workshop included new authors or young researchers, junior faculty at medical colleges and active researchers interested in scientific writing. This workshop was particularly important since the Medical Council of India has recently decided that research papers will be mandatory for promotions among faculty in medical colleges. Young researchers were given an idea as to what to do and how to avoid ethical infractions during the preparation of their research papers. The faculty consisted of experienced editors from indexed Indian medical journals.	</br></br>
