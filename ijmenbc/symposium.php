<h3 align="center">Symposium for Ethics Committee Members</h3>
<font face="arial" size="2" >
November 17, 2010; 8.30 am to 1.00 pm</b></font><br>
Members of Institutional Review Boards, Ethics Committees, and others interested in research ethics and governance in India participated in this interactive symposium. The focus of the symposium was to help build capacity, accountability and common standards for ethical review of research in India and the region and work together on a common platform with the Indian Council of Medical Research (ICMR) Bioethics Initiative; The Forum for Ethical Review Committees in India (FERCI), The Forum for Ethical Review Committees in the Asian and Western Pacific Region (FERCAP) and The Strategic Initiative for Developing Capacity in Ethical Review (SIDCER). 
